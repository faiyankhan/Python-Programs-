# Arithmatic Operators in python 
num1 = 44
num2 = 9
print("num1 + num2 is ", num1 + num2)
print("num1 - num2 is ", num1 - num2)
print("num1 * num2 is ", num1 * num2)
print("num1 / num2 is ", num1 / num2)
print("num1 // num2 is ", num1 // num2)
print("num1 ** num2 is ", num1 **num2)
print("num1 % num2 is ", num1 % num2)

